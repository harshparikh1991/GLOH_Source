using System;

public class NewLoadingText
{
	public string m_szTextKey = string.Empty;

	public int m_nMinLV;

	public int m_nMaxLV;

	public string m_szBundlePath = string.Empty;

	public int m_nShowType;

	public NewLoadingText()
	{
		this.m_szTextKey = string.Empty;
		this.m_nMinLV = 0;
		this.m_nMaxLV = 0;
		this.m_szBundlePath = string.Empty;
		this.m_nShowType = 0;
	}
}
